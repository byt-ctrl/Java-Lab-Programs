class BasicArithmeticOperations {
    public static void main(String[] args) {
        
        // integer type operations
        int num1 = 10, num2 = 5;
        System.out.println(); // blank line for separation

        System.out.println("Integer Operations :");
        System.out.println("Addition: " + (num1 + num2));
        System.out.println("Subtraction: " + (num1 - num2));
        System.out.println("Multiplication: " + (num1 * num2));
        System.out.println("Division: " + (num1 / num2));
        System.out.println("Modulus: " + (num1 % num2));
        
        System.out.println(); // blank line for separation
        
        // floating point type operations (float)
        float num3 = 10.5f, num4 = 2.5f;
        System.out.println("Float Operations :");
        System.out.println("Addition: " + (num3 + num4));
        System.out.println("Subtraction: " + (num3 - num4));
        System.out.println("Multiplication: " + (num3 * num4));
        System.out.println("Division: " + (num3 / num4));
        
        System.out.println(); // blank line for separation
        
        // double type operations (double)
        double num5 = 15.75, num6 = 4.25;
        System.out.println("Double Operations :");
        System.out.println("Addition: " + (num5 + num6));
        System.out.println("Subtraction: " + (num5 - num6));
        System.out.println("Multiplication: " + (num5 * num6));
        System.out.println("Division: " + (num5 / num6));
        
        System.out.println(); // blank line for separation
        
        // long type operations
        long num7 = 100000L, num8 = 50000L;
        System.out.println("Long Operations :");
        System.out.println("Addition: " + (num7 + num8));
        System.out.println("Subtraction: " + (num7 - num8));
        System.out.println("Multiplication: " + (num7 * num8));
        System.out.println("Division: " + (num7 / num8));
    }
}
