class SumOfTwoNumbers {
    public static void main(String[] args) {
        // define two numbers
        int num1 = 10;
        int num2 = 20;

        // calculate the sum of the two numbers
        int sum = num1 + num2 ;

        // print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is : " + sum);
    }
}
