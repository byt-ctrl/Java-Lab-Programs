class InchesToMeters {
    public static void main(String[] args) {

        // define a static value in inches (you can change this to any number)
        float inches = 70;

        // convert inches to meters using the formula
        float meters = inches / 39.37;

        // output the result
        System.out.println(inches + " inches is equal to " + meters + " meters.");
    }
}
